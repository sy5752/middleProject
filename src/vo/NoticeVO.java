package vo;

public class NoticeVO {

	private int noti_no;
	private String noti_title;
	private String noti_cont;
	private String noti_date;
	private String admin_id;
	
	
	public int getNoti_no() {
		return noti_no;
	}
	public void setNoti_no(int noti_no) {
		this.noti_no = noti_no;
	}
	public String getNoti_title() {
		return noti_title;
	}
	public void setNoti_title(String noti_title) {
		this.noti_title = noti_title;
	}
	public String getNoti_cont() {
		return noti_cont;
	}
	public void setNoti_cont(String noti_cont) {
		this.noti_cont = noti_cont;
	}
	public String getNoti_date() {
		return noti_date;
	}
	public void setNoti_date(String noti_date) {
		this.noti_date = noti_date;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	
}
