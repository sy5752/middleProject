package vo;

public class FacVO {
	private String fac_no;
	private int fac_count;
	private double fac_sum;
	
	public String getFac_no() {
		return fac_no;
	}
	public void setFac_no(String fac_no) {
		this.fac_no = fac_no;
	}
	public int getFac_count() {
		return fac_count;
	}
	public void setFac_count(int fac_count) {
		this.fac_count = fac_count;
	}
	public double getFac_sum() {
		return fac_sum;
	}
	public void setFac_sum(int fac_sum) {
		this.fac_sum = fac_sum;
	}
	
	
	
	
	
	
	

}
